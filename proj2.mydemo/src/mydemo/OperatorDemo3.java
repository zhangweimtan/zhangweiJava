package mydemo;
/*
    运算符号：
    / 除法
    % 除余
 */
public class OperatorDemo3 {
    public static void main(String[] args) {
//        定义变量
        int a = 10;
        int b = 3;
        double c = a;
        System.out.println(c / b);
        System.out.println(a % b);
        System.out.println(10.0 / b);
    }
}
