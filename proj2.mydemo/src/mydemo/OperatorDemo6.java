package mydemo;
/*
    关系运算符：
    ==， >=, <=, !=
    结果是boolean类型
 */
public class OperatorDemo6 {
    public static void main(String[] args) {
//        定义变量
        int a = 10;
        int b = 20;
        int c = 10;
//        == != <=
        System.out.println(a == b);
        System.out.println(a == c);
        System.out.println(a != b);
        System.out.println(a <= b);
    }
}
