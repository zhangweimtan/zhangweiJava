package com.mytest_for_03;
/*
    获取数据1-5和5-1
 */
public class ForTest {
    public static void main(String[] args) {
//        获取数据1-5
        for(int a = 1; a<=5; a++) {
            System.out.println(a);
        }
        System.out.println("--------");
        for(int b = 5; b >=1; b--) {
            System.out.println(b);
        }
    }
}
